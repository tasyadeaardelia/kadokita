    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $banner = Banner::find($id);
        if(\File::exists(public_path('library/banner/'.$banner['file']))) {
            \File::delete(public_path('library/banner/'.$banner['file']));
            $banner->delete();
            if($banner) {
                return redirect()->route('banner.index')->with('sukses', 'Data berhasil ditambahkan');
            }
            else {
                return redirect()->back()->with('gagal', 'Data tidak berhasil ditambahkan');
            }
        }
    }
